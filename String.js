
let text = "John Doe";
console.log(text);

// to find the length of the string.

let length = text.length;
console.log(length);

// to find the character at a specific index in a String

let char = text.charAt(0);
console.log(char);

// use of at(), work same as charAt();

const Name = "Maria";
let letter = Name.at(2);
console.log(letter);

//Slice() method to extract a String.

let fruit = "Apple, Banana, Kiwi";
let part = fruit.slice(7, 13);
console.log(part);

//subString() method.

let Part = fruit.substring(7, 13);
console.log(Part);

// substr()method.

let PART = fruit.substr(7, 6);
console.log(PART);

